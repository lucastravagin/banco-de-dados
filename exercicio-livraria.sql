/* Criando uma tabela */
CREATE TABLE LIVROS(
	NOME_LIVRO VARCHAR(100),
    NOME_AUTOR VARCHAR(100),
    SEXO_AUTOR CHAR(1),
    NUMERO_PAGINAS INT,
    EDITORA VARCHAR(50),
    ESTADO_EDITORA CHAR(2),
    ISBN VARCHAR(11),
    VALOR DECIMAL(18,6)
);

